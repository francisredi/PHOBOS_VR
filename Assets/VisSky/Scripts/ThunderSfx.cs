using UnityEngine;
using System.Collections;

public class ThunderSfx : MonoBehaviour {
//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------

    AudioSource sound;
    
    [HideInInspector] public float sfxVolume;

//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------
void Start()
{
    sound = GetComponent<AudioSource>(); 
    sound.volume = sound.volume * sfxVolume;
    sound.Play();
}
//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------
void Update()
{
    if(!sound.isPlaying)
        Destroy(gameObject);
}
//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------
//--------------------------------------------------------------------------------------------
}
