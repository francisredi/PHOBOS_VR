using UnityEngine;
using System.Collections;

public class WeatherSfx : MonoBehaviour {
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------

    AudioSource[] weatherSounds;

    int WIND = 0;
    int RAIN = 1;

    VisSky visky;

//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Start()
{
    visky = gameObject.transform.parent.GetComponent<VisSky>();
    weatherSounds = gameObject.GetComponents<AudioSource>();
}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
void Update()
{
    //----------------------------------------------------------------
    weatherSounds[WIND].volume = visky.sfxVolume * visky.windIntensity * 0.5f;
    weatherSounds[RAIN].volume = visky.sfxVolume * visky.rainIntensity;
    //----------------------------------------------------------------
}
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
//---------------------------------------------------------------------------------------------------------------------
}
